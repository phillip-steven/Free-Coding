// Mendefinisikan konsanta
const PI = 3.141492653589793;

// Jari-jari lingkaran
var radius = 5.0;

// Mendeklarasikan variabel luas dan keliling
var luas, keliling;

// Menghitung luas lingkaran
luas = PI * radius * radius;

// Menghitung keliling lingkaran
keliling = 2 * PI * radius;

// Menampilkan luas dan keliling lingkaran
console.log("Luas lingkaran\t\t: " + luas);
console.log("Keliling lingkaran\t\t: " + keliling);
